package com.etoak.util;

public class Globals {

	//职务
	public static Integer ZHIWU_JINGLI = 40;
	public static Integer ZHIWU_FENGUANJINGLI = 30;
	public static Integer ZHIWU_ZHONGCENGZHENGZHI = 10;
	public static Integer ZHIWU_ZHONGCENGFUZHI = 5;
	public static Integer ZHIWU_NONE = 0;
	
	//职称
	public static Integer ZHICHENG_GAOJI = 100;
	public static Integer ZHICHENG_ZHONGJI = 60;
	public static Integer ZHICHENG_CHUJI = 30;
	public static Integer ZHICHENG_NONE = 0;
	
	public static Integer selectScoreByKey(String key){
		
		if("经理".equals(key)) return Globals.ZHIWU_JINGLI;
		else if("分管经理".equals(key)) return Globals.ZHIWU_FENGUANJINGLI;
		else if("中层正职".equals(key)) return Globals.ZHIWU_ZHONGCENGZHENGZHI;
		else if("中层副职".equals(key)) return Globals.ZHIWU_ZHONGCENGFUZHI;
		else if("其他".equals(key)) return Globals.ZHIWU_NONE;
		
		else if("高级".equals(key)) return Globals.ZHICHENG_GAOJI;
		else if("中级".equals(key)) return Globals.ZHICHENG_ZHONGJI;
		else if("初级".equals(key)) return Globals.ZHICHENG_CHUJI;
		else if("无职称".equals(key)) return Globals.ZHICHENG_NONE;
		
		else if("一年".equals(key)) return 2;
		else if("两年".equals(key)) return 4;
		else if("三年".equals(key)) return 6;
		else if("四年".equals(key)) return 8;
		else if("五年".equals(key)) return 10;
		else if("六年".equals(key)) return 12;
		else if("七年".equals(key)) return 14;
		else if("八年".equals(key)) return 16;
		else if("九年".equals(key)) return 18;
		else if("十年".equals(key)) return 20;
		else if("十一年".equals(key)) return 22;
		else if("十二年".equals(key)) return 24;
		else if("十三年".equals(key)) return 26;
		else if("十四年".equals(key)) return 28;
		else if("十五年".equals(key)) return 30;
		else if("十六年".equals(key)) return 32;
		else if("十七年".equals(key)) return 34;
		else if("十八年".equals(key)) return 36;
		else if("十九年".equals(key)) return 38;
		else if("二十年".equals(key)) return 40;	
		
		return 0;
	}
	
	
	
	
}
